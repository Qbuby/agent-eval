async def _pick_provider(repo: Repository, explicit: str | None) -> str | None:
    if explicit:
        return explicit
    providers = await repo.list_evaluator_providers(active_only=True)
    if not providers:
        return None
    return str(providers[0].id)


async def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument(
        "--provider-id",
        default=None,
        help="LLM-judge 评估器用的 evaluator_provider UUID；不传则自动选第一个 active。",
    )
    args = ap.parse_args()

    async with async_session_factory() as session:
        repo = Repository(session)

        provider_id = await _pick_provider(repo, args.provider_id)
        if provider_id is None:
            print(
                "[bootstrap] 没有可用的 evaluator_provider（active）。"
                "所有评估器均为 LLM-judge，需先在「Judge Providers」页创建一个再重跑。",
                file=sys.stderr,
            )
            return 1
        print(f"[bootstrap] checklist 评估器将绑定 provider_id={provider_id}")

        existing = {r.name for r in await repo.list_evaluator_configs()}
        specs = _build_specs(provider_id)

        created, skipped = 0, 0
        for spec in specs:
            if spec["name"] in existing:
                print(f"[bootstrap] SKIP（已存在）: {spec['name']}")
                skipped += 1
                continue

            row = await repo.create_evaluator_config(
                name=spec["name"],
                tag=spec["tag"],
                evaluator_type="configurable_judge",
                description=spec["description"],
                params=spec["params"],
                is_active=True,
            )
            # configurable_judge 需要 seed v1 版本，run 才能 pin 到稳定 version。
            version = await repo.create_evaluator_version(
                evaluator_id=row.id,
                params=spec["params"],
                description="bootstrap checklist v1",
            )
            row.current_version_id = version.id
            await session.flush()
            created += 1
            print(f"[bootstrap] CREATED: {spec['name']}  (tag={spec['tag']}, checklist)")

        await session.commit()
        print(f"[bootstrap] done. created={created} skipped={skipped}")
    return 0


if __name__ == "__main__":
    raise SystemExit(asyncio.run(main()))
